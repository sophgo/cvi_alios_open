static inline void dw_dma_ch_en(dw_dma_axi_reg_t *dma, uint32_t ch_id, bool en)
{
    uint64_t reg_val = 0U;
    uint32_t we_ops = ch_id + 16U;
    uint32_t en_ops = ch_id;

    if (ch_id >= 16UL) {
        we_ops = ch_id + 32U;
        en_ops = ch_id + 16U;
    }

    reg_val |= 1UL << we_ops;

    if (en) {
        reg_val |= 1UL << en_ops;
    }

    dma->CHEN2 = reg_val;
}

